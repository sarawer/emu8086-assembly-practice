.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 0AH,0DH,"Vowel$"
MSG2 DB 0AH,0DH,"Consonant$"
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV AH,1
INT 21H

CMP AL,'A'
JE Vowel

CMP AL,'a'
JE Vowel

CMP AL,'E'
JE Vowel

CMP AL,'e'
JE Vowel

CMP AL,'I'
JE Vowel

CMP AL,'i'
JE Vowel

CMP AL,'O'
JE Vowel

CMP AL,'o'
JE Vowel
  
CMP AL,'U'
JE Vowel

CMP AL,'u'
JE Vowel 

LEA DX,MSG2
MOV AH,9
INT 21H  
JMP EXIT

Vowel:
LEA DX,MSG1
MOV AH,9
INT 21H
JMP EXIT
   

EXIT:
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN
