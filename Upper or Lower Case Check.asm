.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 0AH,0DH,"UPPER$"
MSG2 DB 0AH,0DH,"LOWER$"
MSG3 DB 0AH,0DH,"neither$"
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV AH,1
INT 21H


CMP AL,'a'
JGE LOWER
CMP AL,'Z'
JG neither
CMP AL,'A'
JL neither

LEA DX,MSG1
MOV AH,9
INT 21H  
JMP EXIT

LOWER:
CMP AL,'z'
JG neither

LEA DX,MSG2
MOV AH,9
INT 21H
JMP EXIT 

neither:
LEA DX,MSG3
MOV AH,9
INT 21H
JMP EXIT
   

EXIT:
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN
