.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 0AH,0DH,"ODD$"
MSG2 DB 0AH,0DH,"EVEN$"
X DB 2
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV AH,1
INT 21H
SUB AL,30H

MOV AH,0
DIV X

CMP AH,0
JE EVEN

LEA DX,MSG1
MOV AH,9
INT 21H  
JMP EXIT

EVEN:
LEA DX,MSG2
MOV AH,9
INT 21H
JMP EXIT
   

EXIT:
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN
