.STACK 100H
.DATA
MSG1 DB 0AH,0DH,"ENTER A:$"
MSG2 DB 0AH,0DH,"ENTER B:$"
MSG3 DB 0AH,0DH,"A+B=$"

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG1
MOV AH,9
INT 21H

MOV AH,1
INT 21H
SUB AL,30H
MOV BH,AL

LEA DX,MSG2
MOV AH,9
INT 21H

MOV AH,1
INT 21H
SUB AL,30H
ADD AL,BH
MOV CH,AL

LEA DX,MSG3
MOV AH,9
INT 21H

ADD CH,30H
